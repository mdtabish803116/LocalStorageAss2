function displayData(mensData){
       mensData.forEach(function(menItem ){
          var menCart = document.createElement("div");
            var img = document.createElement("img");
              img.src = menItem.img_url;
             var name = document.createElement("p");
               name.textContent = menItem.name;
              var price = document.createElement("p");
                 price.textContent = `Rs.${menItem.price}`;
            var cartBtn = document.createElement("button");
                cartBtn.textContent = "Add to cart";

             cartBtn.addEventListener("click" , function(){
                 addToCart(menItem);
             });
            menCart.append(img , name , price , cartBtn);
          document.getElementById("container").append(menCart);

       })
}

