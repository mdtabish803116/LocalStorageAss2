function displayData(mensData){
       mensData.forEach(function(womenItem){
          var menCart = document.createElement("div");
            var img = document.createElement("img");
              img.src = womenItem.img_url;
             var name = document.createElement("p");
               name.textContent = womenItem.name;
              var price = document.createElement("p");
                 price.textContent = `Rs.${womenItem.price}`;
            var cartBtn = document.createElement("button");
                cartBtn.textContent = "Add to cart";

             cartBtn.addEventListener("click" , function(){
                 addToCart(womenItem)
             });
            menCart.append(img , name , price , cartBtn);
          document.getElementById("container").append(menCart);

       })
}

var cartCount = document.getElementById("countCart");

   if(localStorage.getItem("cartItems") === null){
       localStorage.setItem("cartItems" , JSON.stringify([]));
       cartCount.textContent = "0";
   }else {
       cartCount.textContent = JSON.parse(localStorage.getItem("cartItems")).length;
   }
    
function addToCart(womenItem){
    var cartItems = JSON.parse(localStorage.getItem("cartItems"));
       var flag = true;
    cartItems.forEach(function(item){
         if(item.id === womenItem.id){
             flag = false;
         }
    })

    if(flag == false){
        alert("You have already add this product")
    }else {
        cartItems.push(womenItem);
        alert("you have added to the cart succesfully");
        localStorage.setItem("cartItems" , JSON.stringify(cartItems));

        cartCount.textContent = JSON.parse(localStorage.getItem("cartItems")).length
    }

}


